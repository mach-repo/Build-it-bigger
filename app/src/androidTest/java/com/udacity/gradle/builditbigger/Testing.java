@RunWith(AndroidJUnit4.class)
@LargeTest
public class Testing {


    @Rule
    public IntentsTestRule<MainActivity> mActivityTestRule = new IntentsTestRule<MainActivity>(MainActivity.class);

    @Before
    public void stubAllExternalIntents() {
        // By default Espresso Intents does not stub any Intents. Stubbing needs to be setup before
        // every test run. In this case all external Intents will be blocked.
        intending(not(isInternal())).respondWith(new Instrumentation.ActivityResult(Activity.RESULT_OK, null));
    }

    @Test
    public void checkButton_JokeTellingActivity() {
        // click on tell joke button
        onView(withId(R.id.joke_button)).perform(click());

        // is this the JokeTellingActivity ?
        intended(hasComponent(JokeTellingActivity.class.getName()));
    }

}
